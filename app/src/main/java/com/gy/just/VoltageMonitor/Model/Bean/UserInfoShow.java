public class UserInfoShow {
    private String name;
    private String tel;
    private String mail;
    private String local;
    private String eploy;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getTel() {
        return tel;
    }

    public void setTel(String tel) {
        this.tel = tel;
    }

    public String getMail() {
        return mail;
    }

    public void setMail(String mail) {
        this.mail = mail;
    }

    public String getLocal() {
        return local;
    }

    public void setLocal(String local) {
        this.local = local;
    }

    public String getEploy() {
        return eploy;
    }

    public void setEploy(String eploy) {
        this.eploy = eploy;
    }
}
